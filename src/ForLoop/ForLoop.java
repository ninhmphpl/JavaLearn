package ForLoop;

public class ForLoop {
    public static void main(String[] args) {
//        for loop
        for (int i = 0 ; i < 10 ; i++){
            System.out.println(i);
        }
//        for-each
        int[] arr = {1,3,4,5,4,4,1,1};
        for (int a : arr){
            System.out.println(a);
        }
    }
}
